#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*new_string;
	size_t	i;

	new_string = (char *)ft_calloc(ft_strlen(s) + 1, sizeof(char));
	if (!new_string)
		return (0);
	i = 0;
	while (s[i])
	{
		new_string[i] = s[i];
		i++;
	}
	new_string[i] = '\0';
	return (new_string);
}
